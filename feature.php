<script>
    const menuItems = document.querySelectorAll(".features-menu li");
    const sections = document.querySelectorAll(".scroll-section");

    // Menu click → smooth scroll
    menuItems.forEach(item => {
        item.addEventListener("click", () => {
            const target = document.getElementById(item.dataset.target);
            const topPos = target.offsetTop - 80; // adjust header offset
            window.scrollTo({
                top: topPos,
                behavior: "smooth"
            });
        });
    });

    // Scroll → highlight active menu
    window.addEventListener("scroll", () => {
        let currentSection = "";

        sections.forEach(section => {
            const rect = section.getBoundingClientRect();

            // this makes item active when section enters middle of screen
            if (rect.top <= window.innerHeight * 0.3 && rect.bottom >= window.innerHeight * 0.3) {
                currentSection = section.id;
            }
        });

        menuItems.forEach(li => {
            li.classList.toggle("active", li.dataset.target === currentSection);
        });
    });
</script>

